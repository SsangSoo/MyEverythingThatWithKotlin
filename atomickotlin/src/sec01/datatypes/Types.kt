package sec01.datatypes

fun main() {
    val whole : Int = 11                        // 11
    val fractional : Double = 1.4               // 1.4
    val trueOfFalse : Boolean = true            // true
    val words : String = "A value"              // A value
    val character : Char = 'z'                  // z
    val lines : String = """Triple quotes let       
                            you have many lines
                            in your string"""
//    Triple quotes let
//    you have many lines
//    in your string

    println(whole)
    println(fractional)
    println(trueOfFalse)
    println(words)
    println(character)
    println(lines)
}